package cl.cstit.msd.ccs.vo;

public class SetupOpConsolidationVO {
	
	
	private String idConsolidation;
	private int    orderConsolidation;
	private String idStageConsolidation;
	private String idSiteSource;
	private String nameSiteSource;
	
	
	public String getIdConsolidation() {
		return idConsolidation;
	}
	public void setIdConsolidation(String idConsolidation) {
		this.idConsolidation = idConsolidation;
	}
	public int getOrderConsolidation() {
		return orderConsolidation;
	}
	public void setOrderConsolidation(int orderConsolidation) {
		this.orderConsolidation = orderConsolidation;
	}
	public String getIdStageConsolidation() {
		return idStageConsolidation;
	}
	public void setIdStageConsolidation(String idStageConsolidation) {
		this.idStageConsolidation = idStageConsolidation;
	}
	public String getIdSiteSource() {
		return idSiteSource;
	}
	public void setIdSiteSource(String idSiteSource) {
		this.idSiteSource = idSiteSource;
	}
	public String getNameSiteSource() {
		return nameSiteSource;
	}
	public void setNameSiteSource(String nameSiteSource) {
		this.nameSiteSource = nameSiteSource;
	}

}
