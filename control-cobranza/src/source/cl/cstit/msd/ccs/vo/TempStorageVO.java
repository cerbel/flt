package cl.cstit.msd.ccs.vo;

public class TempStorageVO {

	private String sstId;
	private String entry;
	private String percentCost;
	private String processingFee;
	private String idStage;
	private String currencyID;
	
	
	public String getSstId() {
		return sstId;
	}
	public void setSstId(String sstId) {
		this.sstId = sstId;
	}
	public String getEntry() {
		return entry;
	}
	public void setEntry(String entry) {
		this.entry = entry;
	}
	public String getPercentCost() {
		return percentCost;
	}
	public void setPercentCost(String percentCost) {
		this.percentCost = percentCost;
	}
	public String getProcessingFee() {
		return processingFee;
	}
	public void setProcessingFee(String processingFee) {
		this.processingFee = processingFee;
	}
	public String getIdStage() {
		return idStage;
	}
	public void setIdStage(String idStage) {
		this.idStage = idStage;
	}
	public String getCurrencyID() {
		return currencyID;
	}
	public void setCurrencyID(String currencyID) {
		this.currencyID = currencyID;
	}

	
	
}
