package cl.cstit.msd.ccs.vo;

public class CustodyVO {
	
	private float  baseLineCustody;
	private float  feeCostCustody;
	private String idCurrencyCustody;
	private String idStageCustody;
	
	public float getBaseLineCustody() {
		return baseLineCustody;
	}
	public void setBaseLineCustody(float baseLineCustody) {
		this.baseLineCustody = baseLineCustody;
	}
	public float getFeeCostCustody() {
		return feeCostCustody;
	}
	public void setFeeCostCustody(float feeCostCustody) {
		this.feeCostCustody = feeCostCustody;
	}
	public String getIdCurrencyCustody() {
		return idCurrencyCustody;
	}
	public void setIdCurrencyCustody(String idCurrencyCustody) {
		this.idCurrencyCustody = idCurrencyCustody;
	}
	public String getIdStageCustody() {
		return idStageCustody;
	}
	public void setIdStageCustody(String idStageCustody) {
		this.idStageCustody = idStageCustody;
	}
	
}
