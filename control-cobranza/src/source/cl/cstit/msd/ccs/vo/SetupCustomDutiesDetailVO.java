package cl.cstit.msd.ccs.vo;

public class SetupCustomDutiesDetailVO {
	
	private String idDutyDetail;
	private String nameDutyDetail;
	private float  tariffDutyDetail;
	private float  otherTaxesDutyDetail;
	private String idDutyHeadDutyDetail;
	private String codeDuty;
	
	public String getIdDutyDetail() {
		return idDutyDetail;
	}
	public void setIdDutyDetail(String idDutyDetail) {
		this.idDutyDetail = idDutyDetail;
	}
	public String getNameDutyDetail() {
		return nameDutyDetail;
	}
	public void setNameDutyDetail(String nameDutyDetail) {
		this.nameDutyDetail = nameDutyDetail;
	}
	public float getTariffDutyDetail() {
		return tariffDutyDetail;
	}
	public void setTariffDutyDetail(float tariffDutyDetail) {
		this.tariffDutyDetail = tariffDutyDetail;
	}
	public float getOtherTaxesDutyDetail() {
		return otherTaxesDutyDetail;
	}
	public void setOtherTaxesDutyDetail(float otherTaxesDutyDetail) {
		this.otherTaxesDutyDetail = otherTaxesDutyDetail;
	}
	public String getIdDutyHeadDutyDetail() {
		return idDutyHeadDutyDetail;
	}
	public void setIdDutyHeadDutyDetail(String idDutyHeadDutyDetail) {
		this.idDutyHeadDutyDetail = idDutyHeadDutyDetail;
	}
	public String getCodeDuty() {
		return codeDuty;
	}
	public void setCodeDuty(String codeDuty) {
		this.codeDuty = codeDuty;
	}
	
}
