package cl.cstit.msd.ccs.vo;

public class SetupExchangeVO {
	
	
	private String idExchange;
	private String nameExchange;
	private float  amountExchange;
	private String descriptionExchange;
	private String idStageExchange;
	private String acronymExchage;
	
	
	public String getAcronymExchage() {
		return acronymExchage;
	}
	public void setAcronymExchage(String acronymExchage) {
		this.acronymExchage = acronymExchage;
	}
	public String getIdExchange() {
		return idExchange;
	}
	public void setIdExchange(String idExchange) {
		this.idExchange = idExchange;
	}
	public String getNameExchange() {
		return nameExchange;
	}
	public void setNameExchange(String nameExchange) {
		this.nameExchange = nameExchange;
	}
	public float getAmountExchange() {
		return amountExchange;
	}
	public void setAmountExchange(float amountExchange) {
		this.amountExchange = amountExchange;
	}
	public String getDescriptionExchange() {
		return descriptionExchange;
	}
	public void setDescriptionExchange(String descriptionExchange) {
		this.descriptionExchange = descriptionExchange;
	}
	public String getIdStageExchange() {
		return idStageExchange;
	}
	public void setIdStageExchange(String idStageExchange) {
		this.idStageExchange = idStageExchange;
	}
}
