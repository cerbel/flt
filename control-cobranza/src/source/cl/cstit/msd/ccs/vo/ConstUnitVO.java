package cl.cstit.msd.ccs.vo;

public class ConstUnitVO {
	
	private String idCost;
	private String nameCost;
	private float  valueCost;
	
	public String getIdCost() {
		return idCost;
	}
	public void setIdCost(String idCost) {
		this.idCost = idCost;
	}
	public String getNameCost() {
		return nameCost;
	}
	public void setNameCost(String nameCost) {
		this.nameCost = nameCost;
	}
	public float getValueCost() {
		return valueCost;
	}
	public void setValueCost(float valueCost) {
		this.valueCost = valueCost;
	}
}
